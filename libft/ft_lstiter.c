#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*temp;

	if (!lst || !f)
		return ;
	temp = lst;
	while (lst)
	{
		temp = lst;
		f(temp->content);
		lst = temp->next;
	}
}
